var fixedText = '';
var round = 0;
var square = 0;
var curly = 0;

for(var i = 0; i < text.length; i++){

	if(text[i] === '('){
		round++;
		fixedText += '(';
	}

	else if(text[i] === '['){
		square++;
		fixedText += '[';
	}

	else if(text[i] === '{'){
		curly++;
		fixedText += '{';
	}

	else if(text[i] === ')'){
		if (round){
			round--;
			fixedText += ')';
		}
		else{
			fixedText += '()';
		}
	}

	else if(text[i] === ']'){
		if (square){
			square--;
			fixedText += ']';
		}
		else{
			fixedText += '[]';
		}
	}

	else if(text[i] === '}'){		
		if (curly){
			curly--;
			fixedText += '}';
		}
		else{
			fixedText += '{}';
		}
	}
	
}

for(var i = 0; i < round; i++){
	fixedText += ')';
}

for(var i = 0; i < square; i++){
	fixedText += ']';
}

for(var i = 0; i < curly; i++){
	fixedText += '}';
}

var textarea = document.getElementsByName('answer')[0];
textarea.value = fixedText;

var button = document.getElementsByTagName('button')[1];
button.click();

